    /// <summary>
    /// Выполните шаги 1a или 1b, а затем 2, чтобы использовать этот пользовательский элемент управления в файле XAML.
    ///
    /// Шаг 1a. Использование пользовательского элемента управления в файле XAML, существующем в текущем проекте.
    /// Добавьте атрибут XmlNamespace в корневой элемент файла разметки, где он 
    /// будет использоваться:
    ///
    ///     xmlns:MyNamespace="clr-namespace:WpfRedactor"
    ///
    ///
    /// Шаг 1б. Использование пользовательского элемента управления в файле XAML, существующем в другом проекте.
    /// Добавьте атрибут XmlNamespace в корневой элемент файла разметки, где он 
    /// будет использоваться:
    ///
    ///     xmlns:MyNamespace="clr-namespace:WpfRedactor;assembly=WpfRedactor"
    ///
    /// Потребуется также добавить ссылку из проекта, в котором находится файл XAML,
    /// на данный проект и пересобрать во избежание ошибок компиляции:
    ///
    ///     Щелкните правой кнопкой мыши нужный проект в обозревателе решений и выберите
    ///     "Добавить ссылку"->"Проекты"->[Поиск и выбор проекта]
    ///
    ///
    /// Шаг 2)
    /// Теперь можно использовать элемент управления в файле XAML.
    ///
    ///     <MyNamespace:TextBoxWithClear/>
    ///
    /// </summary>
    
    [TemplatePart(Name = "PART_Button",Type = typeof(Button))]
    public class TextBoxWithClear : TextBox
    {
        static TextBoxWithClear()
        {
            DefaultStyleKeyProperty.OverrideMetadata(typeof(TextBoxWithClear), new FrameworkPropertyMetadata(typeof(TextBoxWithClear)));
        }

        public override void OnApplyTemplate()
        {
            base.OnApplyTemplate();

            var button = GetTemplateChild("PART_Button") as Button;
            if (button != null)
            {
                button.Click += Button_Click;
            }


        }

        private void Button_Click(object sender, RoutedEventArgs e)
        {
            Text = string.Empty;
        }
    }
}
